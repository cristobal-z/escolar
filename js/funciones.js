  function especialidad(){
    cla= $('#cla').val();
    espe= $('#esp').val();

    cadena="id="+cla+
            "&especialidad="+espe;

        $.ajax({
          type:"POST",
          url:"php/agregarEspecialidad.php",
          data:cadena,
          success:function(r){
            if (r==0) {
              $('#tabla').load('componentes/tabla.php')

              alert('Datos guardados');
            }else {
              alert('error');
            }
          }
        });
  }

